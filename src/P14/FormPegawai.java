    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
    String namaPegawai = jTextField2.getText().trim();
    String nik = jTextField3.getText().trim();
    String alamat = jTextArea1.getText().trim();
    String noHp = jTextField4.getText().trim();
    String email = jTextField5.getText().trim();
    String jenisKelamin = (String) jComboBox1.getSelectedItem();
    String jabatan = (String) jComboBox2.getSelectedItem();
        int selectedRow = jTable1.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin edit pegawai ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        int idPegawai = (int) jTable1.getValueAt(selectedRow, 0); // ambil ID di kolom 0

        try (Connection conn = DatabaseConnection.connect();
             PreparedStatement stmt = conn.prepareStatement("UPDATE pegawai SET nama_pegawai=?,nik=?, jenis_kelamin=?, alamat=?, no_hp=?, email=?, jabatan=? WHERE id_pegawai = ?")) {

            
            stmt.setString(1, namaPegawai);
            stmt.setString(2, nik);
            stmt.setString(3, jenisKelamin);
            stmt.setString(4, alamat);
            stmt.setString(5, noHp);
            stmt.setString(6, email);
            stmt.setString(7, jabatan);
            stmt.setInt(8, idPegawai);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data Pegawai berhasil diedit.");
            tampilkanDataPegawai(); // refresh tabel

        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Gagal mengedit data: " + ex.getMessage());
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

