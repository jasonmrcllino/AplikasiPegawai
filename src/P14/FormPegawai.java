    private void tampilkanDataPegawai() {
    try {
        Connection conn = P13.DatabaseConnection.connect();
        String sql = "SELECT id_pegawai, nama_pegawai, nik, jenis_kelamin, alamat, no_hp, email, jabatan FROM pegawai";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Pegawai");
        model.addColumn("Nama Pegawai");
        model.addColumn("nik");
        model.addColumn("jenis_kelamin");
        model.addColumn("alamat");
        model.addColumn("no_hp");
        model.addColumn("email");
        model.addColumn("Jabatan");
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_pegawai"),
                rs.getString("nama_pegawai"),
                rs.getString("nik"),
                rs.getString("jenis_kelamin"),
                rs.getString("alamat"),
                rs.getString("no_hp"),
                rs.getString("email"),
                rs.getString("jabatan")
                     });
        }
            jTable1.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

